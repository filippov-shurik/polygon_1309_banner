void I2C2_EV_IRQHandler(void)
{
	/* USER CODE BEGIN I2C2_EV_IRQn 0 */
	/* USER CODE END I2C2_EV_IRQn 0 */
	/* USER CODE BEGIN I2C2_EV_IRQn 1 */
	/* Check ADDR flag value in ISR register */
	if (LL_I2C_IsActiveFlag_ADDR(I2C2))
	{
		/* Verify the transfer direction, a write direction, Slave enters receiver mode */
		if (LL_I2C_GetTransferDirection(I2C2) == LL_I2C_DIRECTION_WRITE) //主机读
		{
			/* Enable Buffer Interrupts */
			LL_I2C_EnableIT_BUF(I2C2);
			/* Clear ADDR flag value in ISR register */
			LL_I2C_ClearFlag_ADDR(I2C2);
		}
		else if (LL_I2C_GetTransferDirection(I2C2) == LL_I2C_DIRECTION_READ) //主机写
		{
			/* Enable Buffer Interrupts */
			LL_I2C_EnableIT_BUF(I2C2);
			/* Clear ADDR flag value in ISR register */
			LL_I2C_ClearFlag_ADDR(I2C2);
		}
		else
		{
			/* Clear ADDR flag value in ISR register */
			LL_I2C_ClearFlag_ADDR(I2C2);
		}
	}
	/* Check TXE flag value in ISR register */
	if (LL_I2C_IsActiveFlag_TXE(I2C2))
	{
		//LL_I2C_TransmitData8(I2C2, aTXBuffer[ubTXIndex++]);
	}
	/* Check BTF flag value in ISR register */
	if (LL_I2C_IsActiveFlag_BTF(I2C2))
	{
		//LL_I2C_TransmitData8(I2C2, aTXBuffer[ubTXIndex++]);
	}
	/* Check RXNE flag value in ISR register */
	if (LL_I2C_IsActiveFlag_RXNE(I2C2))
	{
		/* Call function Slave Reception Callback */
		uint8_t temp = LL_I2C_ReceiveData8(I2C2);
		if (isDownload == 1)
		{
			if (download_index < 1024 - 1)
			{
				xbm_data[download_index] = temp;
				download_index++;
			}
			else
			{
				download_index = 0;
				isDownload = 0;
			}
		}
		else
		{
			switch (temp)
			{
			case 0x01:
				status_display = 0x01;
				break;
			case 0x02:
				status_display = 0x02;
				break;
			case 0x03:
				status_display = 0x03;
				break;
			case 0x09:
				isDownload = 1;
				break;
			case display_xbm:
				status_display = display_xbm;
				break;
			default:
				status_display = 0x00;
				break;
			}
			temp = 0;
		}
	}
	/* Check BTF flag value in ISR register */
	if (LL_I2C_IsActiveFlag_BTF(I2C1))
	{
		/* Call function Slave Reception Callback */
	}
	/* Check STOP flag value in ISR register */
	if (LL_I2C_IsActiveFlag_STOP(I2C2))
	{
		/* Clear STOP flag value in ISR register */
		LL_I2C_ClearFlag_STOP(I2C2);
	}
	/* USER CODE END I2C2_EV_IRQn 1 */
}
